export function getBillingCopy(lang) {
  return BILLING_COPY[lang] || BILLING_COPY.en;
}

export function formatVnd(amount, lang = 'en') {
  const value = Number(amount);
  if (!Number.isFinite(value)) return '—';
  return `${new Intl.NumberFormat(lang === 'vi' ? 'vi-VN' : 'en-US').format(value)} ₫`;
}

export function formatBillingDate(value, lang = 'en') {
  if (!value) return '—';
  const date = new Date(value);
  if (Number.isNaN(date.getTime())) return '—';
  return date.toLocaleString(lang === 'vi' ? 'vi-VN' : 'en-US', {
    dateStyle: 'medium',
    timeStyle: 'short',
  });
}

export function amountBucket(amount) {
  if (Number(amount) === 20000) return '20k_vnd';
  if (Number(amount) === 49000) return '49k_vnd';
  return null;
}

export function getSafeCheckoutUrl(value) {
  try {
    const url = new URL(value);
    return url.protocol === 'https:' ? url.toString() : null;
  } catch {
    return null;
  }
}
